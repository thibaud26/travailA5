                Un cahier des charges a été rédigé durant l'hiver <strong>2001/2002</strong>, et a débouché sur la programmation d’une étude « pour un Vercors ouvert à l'outil informatique et à Internet à haut débit », confiée au cabinet Tactis début juin <strong>2002</strong>.<br><br>
                Cette commission spécifique est composée d'élus de l'ensemble du territoire, du Parc, des représentants de l'ensemble des communautés de communes et autres structures intercommunales, des  deux départements (Drôme et Isère) et de la Région. Cette commission a été complétée par un panel de spécialistes (informaticiens, développeur d'applications Internet, utilisateurs avertis...) .<br><br>
                Au niveau local, lutter contre la fracture numérique signifiait que le projet devait être partagé par la population et par les élus locaux.
                La commune s’est engagée en nous fournissant gratuitement un local. 
                En <strong>2003</strong>, la Communauté de communes, récemment créée, a endossé le portage du projet en lui conférant une dimension intercommunale, et nous avons créé une association pour gérer l'animation  du numérique sur le territoire.

